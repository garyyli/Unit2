#gradeCalculator.py - tells user what letter grade they got based off percentage

grade = float(input('Input your grade: '))

if grade>=90:
    print('You earned an A')
elif grade>=80:
    print('You earned an B')
elif grade>=70:
    print('You earned an C')
elif grade>=60:
    print('You earned an D')